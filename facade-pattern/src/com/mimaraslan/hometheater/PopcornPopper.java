package com.mimaraslan.hometheater;

public class PopcornPopper {

	public PopcornPopper(String string) {
		System.out.println("PopcornPopper:" + string);
	}

	public void on() {
		System.out.println("PopcornPopper on");
	}

	public void pop() {
		System.out.println("PopcornPopper pop");
	}

	public void off() {
		System.out.println("PopcornPopper off");
	}
}